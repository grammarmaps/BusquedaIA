    private void btnLabNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLabNuevoActionPerformed
        // TODO add your handling code here:
         ventana vn = new ventana();
          int f = Integer.parseInt(this.txtFilas.getText().toString());
          int c = Integer.parseInt(this.txtColumnas.getText().toString());
           
          if((f>=2 && c>=2)||(f>=1 && c>=2) ||(c>=1 && f>=2))
          {
             
            mz = new char[f][c];
             mz = vn.Laberinto_Random(f,c);
            vn.dibuja_ventana(this.panel1.getGraphics(),f,c,mz);    
            vn.filas = f;
            vn.columnas = c;
            this.button1.enable(true);
          this.button2.enable(true);
          this.button3.enable(true);
          }
          else
          {
              this.panel1.enable(false);
               this.button1.enable(false);
          this.button2.enable(false);
          this.button3.enable(false);
          }
          
    }//GEN-LAST:event_btnLabNuevoActionPerformed

