        ventana vn = new ventana();
        
         int  f = Integer.parseInt(this.txtFilas.getText().toString());
          int c = Integer.parseInt(this.txtColumnas.getText().toString());
          vn.filas = f;
          vn.columnas = c;
          vn.dibuja_ventana(this.panel1.getGraphics(),f,c,mz); 
          
          
          
        int d = vn.DFS(mz); 
        if (d == -1){
            this.labeld.setText("Destino no encontrado.");
         }
        else{
            this.labeld.setText(String.valueOf(d));
       
        }
        vn.movimiento_objeto(this.panel1.getGraphics());        
        
       
